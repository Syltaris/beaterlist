import { useContext, useState } from "react";
import { observer } from "mobx-react-lite";
import { Pane, Text, Button, toaster } from "evergreen-ui";

import { PlaylistStoreContext } from "../stores/playlists";
import { beatSaverSongCache } from "../stores/beatSaver";

const customLevelIdPrefix = "custom_level_";

const PlayerDataFileViewer = ({ jsonFile }) => {
  const playlistStore = useContext(PlaylistStoreContext);
  const [loading, setLoading] = useState(false);

  return (
    <div>
      {jsonFile.localPlayers.map((player, idx) => (
        <Pane
          padding="20px"
          key={idx}
          display="flex"
          flexDirection="column"
          border="default"
          borderRadius="5px"
        >
          <Text>
            ID: {player.playerId} | Name: {player.playerName}
          </Text>
          <Button
            marginTop="10px"
            marginBottom="10px"
            isLoading={loading}
            onClick={async () => {
              setLoading(true);
              const playlist = playlistStore.createNewPlaylist();
              playlist.title = "Favourites";

              const songHashes = player.favoritesLevelIds
                .filter((levelId) => levelId.includes(customLevelIdPrefix))
                .map((levelId) =>
                  levelId.substring(customLevelIdPrefix.length, levelId.length)
                );

              // pre-fetch songData
              const presentSongHashes =
                await beatSaverSongCache.retrieveMultipleSongData(songHashes);

              songHashes
                .filter((hash) => presentSongHashes.includes(hash))
                .forEach((hash) => playlist.addSongByHash(hash));
              playlistStore.savePlaylist(playlist);
              setLoading(false);
              toaster.success("Favourites loaded into playlist successfully.");
            }}
          >
            Import favourite levels into playlist
          </Button>
        </Pane>
      ))}
    </div>
  );
};

export default observer(PlayerDataFileViewer);
